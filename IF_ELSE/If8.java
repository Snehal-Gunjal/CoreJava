/* given an integer input , print whether its odd or even 
input = 7 
output = 7 is odd ;
input = 4 
output = 4 is even */

class If8{
	public static void main( String[]args)
	{
		int x = 7;
		int y = 4 ;
		if( x % 2 == 0 && y % 2 == 0 )
		{
			System.out.println( "The numbers is Even");
		}
		else
		{
			System.out.println(  "The numbers is odd ") ;
		}
	}
}
			
		